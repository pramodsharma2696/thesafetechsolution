        return false;
      }
      if (regxPhoneno.test(mobile_number) == false) {
        alert('Invalid Mobile Number');
        return false;
      }
      if (regEmail.test(email) == false) {
        alert('Invalid Email Address');
        return false;
      }
      if ($("#tnc").prop('checked') == false) {
        alert('Accept Terms & Conditions to continue..');
        return false;
      }

